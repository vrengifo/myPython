# Types and arithmetic function
print(2 + 3)             # addition(+)
print(3 - 1)             # subtraction(-)
print(2 * 3)             # multiplication(*)
print(3 / 2)             # division(/)
print(3 ** 3)            # exponential(**)
print(3 % 2)             # modulus(%)
print(3 // 2)            # Floor division operator(//)
print(1 % 2)             # modulus(%)
print(1 // 2)            # Floor division operator(//)
print([1, 2, 3])         # List
print({'text1':'Example 1', 'text2':'Example 2'}) # Dictionary
print({9.8, 3.1416, 2.7})
